void Inpaint(SDCContext *sdc, SDCBlock *block, DefectMap *defects)
{
  i32 x, y, chan, neighborX, neighborY, i;

  for (y = 0; y < 8; y++) {
    for (x = 0; x < 8; x++) {
      if (HasDefect(defects, x, y)) {
        for (chan = 0; chan < 3; chan++) {
          for (neighborY = y - 1; neighborY < y + 2; neighborY++) {
            if (neighborY < 0 || neighborY >= 8) continue;
            for (neighborX = x - 1; neighborX < x + 2; neighborX++) {
              i8 *sources;
              float *values = (float*)block->values[chan];
              float max = 0.0;
              if (neighborX < 0 || neighborX >= 8) continue;
              sources = inpaintSources[neighborY*8+neighborX];
              for (i = 0; i < 8 && sources[i] >= 0; i++) {
                u8 sourceIndex = sources[i];
                max = Max(max, values[sourceIndex]);
              }
              if (values[(neighborY+1)*10+neighborX+1] < max + sdc->pct30) {
                values[(neighborY+1)*10+neighborX+1] = max + sdc->pct30;
              }
            }
          }
        }
      }
    }
  }
}
