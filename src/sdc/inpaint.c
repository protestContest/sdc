void SetDefect(DefectMap *map, u32 x, u32 y)
{
  map->values[y] |= 1 << x;
  map->count++;
}

bool HasDefect(DefectMap *map, u32 x, u32 y)
{
  return (map->values[y] & (1 << x)) != 0;
}

