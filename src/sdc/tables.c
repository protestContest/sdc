#include "sdc/tables.h"
#include <math.h>

static float logTable[65536];
static u32 invLogHi[256];
static u32 invLogLo[256];
static u16 invLogMaxIndex;

typedef struct {
  u32 count;
  u16 maxIdx;
  u32 hiCount;
  double maxVal;
} BitDepthParams;

static BitDepthParams depth8params =  {   256,  0x00FF,   1,   255.0 };
static BitDepthParams depth12params = {  4096,  0x0FFF,  16,  4095.0 };
static BitDepthParams depth16params = { 65536,  0xFFFF, 256, 65535.0 };

static u32 DoubleToU32(double d)
{
  if (d < 0.0) return 0;
  if (d >= 4294967296.0) return (u32)(-1);
  return (u32)d;
}

void InitTables(u16 bitDepth)
{
  u32 i;
  BitDepthParams *p;
  double scale;

  switch (bitDepth) {
  case 8:
    p = &depth8params;
    break;
  case 12:
    p = &depth12params;
    break;
  case 16:
    p = &depth16params;
    break;
  default:
    return;
  }
  scale = p->maxVal / log10((double)p->count);

  invLogMaxIndex = p->maxIdx;

  for (i = 0; i < p->count; i++) {
    logTable[i] = (float)(scale * log10((double)(i+1)));
  }

  for (i = 0; i < 256; i++) {
    if (i < p->hiCount) {
      double exp = (double)(i << 8) / scale;
      double powVal   = pow(10.0, exp);
      invLogHi[i] = DoubleToU32(0.5 + 16.0 * powVal);
    } else {
        invLogHi[i] = 0;
    }
  }

  for (i = 0; i < 256; i++) {
    double exp = (double)i / scale;
    double powVal   = pow(10.0, exp);
    invLogLo[i] = DoubleToU32(0.5 + 65536.0 * powVal);
  }
}

float Log(u16 n)
{
  return logTable[n];
}

u16 InvLog(double density)
{
  i32 biased;
  u16 idx16;
  u8 hi, lo;
  u64 product;

  if (density < 0.0) {
    density = 0.0;
  }

  biased = (i32)(density + 0.5);
  idx16 = (u16)biased;

  if ((u32)invLogMaxIndex < ((u32)biased & 0xFFFF)) {
    idx16 = invLogMaxIndex;
  }

  hi = (u8)(idx16 >> 8);
  lo = (u8)(idx16 & 0xFF);
  product = (u64)invLogHi[hi] * invLogLo[lo];

  return (u16)(product >> 20) - 1;
}

